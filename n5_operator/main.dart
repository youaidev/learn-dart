
void main() {

  // Multiply
  int a = 13;
  int b = 2;
  int mult = a * b;
  print('mult = $a * $b = $mult');

  // Division
  int int_div = a ~/ b;
  print('int_div = $a ~/ $b = $int_div');
  double double_div = a / b;
  print('double_div = $a / $b = $double_div');

  // Addition
  int add = a + b;
  print('add = $a + $b = $add');

  // Subtraction
  int sub = a - b;
  print('sub = $a - $b = $sub');

  // Modulus
  int mod = a % b;
  print('mod = $a % $b = $mod');

}